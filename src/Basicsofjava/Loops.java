package Basicsofjava;

public class Loops {
    public static void main(String [] args){
        for (int i=0;i<11;i++){
            System.out.println(i);
        }
        int a=0;
        while(a<11){
            System.out.println(a);
            a++;
        }
        int b=0;
        do {
            System.out.println(b);
            b++;
        }while(b<11);
    }

}
